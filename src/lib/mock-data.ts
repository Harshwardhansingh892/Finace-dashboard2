function generatePriceHistory(
  basePrice: number,
  days: number = 30
): PricePoint[] {
  const points: PricePoint[] = [];
  let price = basePrice - 15 + Math.random() * 10;
  for (let i = days; i >= 0; i--) {
    price += (Math.random() - 0.48) * 3;
    price = Math.max(5, Math.min(95, price));
    points.push({
      time: new Date(Date.now() - i * 86400000).toISOString(),
      yesPrice: Math.round(price * 100) / 100,
      volume: Math.floor(Math.random() * 50000) + 5000,
    });
  }
  return points;
}

function generateOrderBook(): OrderBookEntry[] {
  const entries: OrderBookEntry[] = [];
  for (let i = 0; i < 10; i++) {
    entries.push({
      price: 50 + i * 2 + Math.random() * 2,
      quantity: Math.floor(Math.random() * 5000) + 500,
      side: "yes",
    });
    entries.push({
      price: 48 - i * 2 + Math.random() * 2,
      quantity: Math.floor(Math.random() * 5000) + 500,
      side: "no",
    });
  }
  return entries;
}

